#include "fillit.h"

int	check_file(const char *str)
{
	int	i;
	int j;

	i = -1;
	j = 0;
	while (str[++i])
	{
		if (str[i] != '.' || str[i] != '#' || str[i] != '\n')
			return (0);
		if (i % 4 == 0 && str[i] != '\n')
			return (0);
		if (str[i] == '\n' && i % 4 != 0)
			return (0);
		//TODO
	}
}
